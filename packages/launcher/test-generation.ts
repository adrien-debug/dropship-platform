#!/usr/bin/env tsx

import { generateFromTemplateFast, generateFromTemplate, getTemplate } from './src/index';
import type { EcommerceSiteConfig, TemplateVars } from './src/index';
import { writeFile, mkdir } from 'fs/promises';
import { join } from 'path';

async function testPureTemplate() {
  console.log('\n🧪 Test 1: Pure Template (no LLM)');
  console.log('=====================================');
  
  const template = getTemplate('anime');
  if (!template) {
    console.error('❌ Template anime not found');
    return;
  }

  const vars: TemplateVars = {
    brandName: 'AnimeStore',
    tagline: 'Your favorite anime merch',
    niche: 'anime',
    products: [
      { name: 'Naruto Figure', price: 29.99, handle: 'naruto-figure' },
      { name: 'One Piece Poster', price: 19.99, handle: 'one-piece-poster' },
      { name: 'Attack on Titan Hoodie', price: 49.99, handle: 'aot-hoodie' },
    ],
  };

  const start = Date.now();
  const files = generateFromTemplate(template, vars);
  const duration = Date.now() - start;

  console.log(`✅ Generated ${files.size} pages in ${duration}ms`);
  
  for (const [path, code] of files) {
    console.log(`  - ${path}: ${code.length} chars, ${code.split('\n').length} lines`);
    
    // Validation basique
    if (!code.includes('export default')) {
      console.warn(`    ⚠️  Missing "export default" in ${path}`);
    }
    if ((path.includes('page.tsx') && !path.includes('about') && !path.includes('contact')) && !code.includes('getProducts')) {
      console.warn(`    ⚠️  Missing "getProducts" in ${path}`);
    }
  }

  // Sauvegarder dans un dossier de test
  const outputDir = './test-output/pure-template';
  await mkdir(outputDir, { recursive: true });
  
  for (const [filePath, code] of files) {
    const fileName = filePath.replace(/\//g, '_').replace(/^_/, '');
    await writeFile(join(outputDir, fileName), code, 'utf-8');
  }
  
  console.log(`📁 Files saved to ${outputDir}`);
}

